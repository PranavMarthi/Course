#Write a program that calculates the degree Fahrenheit of 20 degrees Celsius.

celsius = 20   # assigns a value of 20 to the variable celsius
fahren = int(celsius * 9/5 + 32)   # using a formula to recalculate the fahrenheit temperature
print ("20 degrees celsius is", fahren, "degrees fahrenheit.")  #output the answer


